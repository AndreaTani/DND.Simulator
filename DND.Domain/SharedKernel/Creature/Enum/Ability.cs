namespace DND.Domain.SharedKernel
{
    /// <summary>
    /// Represents the various abilities or attributes commonly used in role-playing games.
    /// </summary>
    /// <remarks>These abilities are typically used to define a character's strengths and weaknesses in
    /// different areas. For example, <see cref="Strength"/> might represent physical power, while <see
    /// cref="Intelligence"/> could represent mental acuity.</remarks>
    public enum Ability { 
        Strength, 
        Dexterity, 
        Constitution, 
        Intelligence, 
        Wisdom, 
        Charisma 
    }
}
